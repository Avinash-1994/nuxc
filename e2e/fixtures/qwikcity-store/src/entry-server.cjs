'use strict';

/**
 * qwikcity-store/src/entry-server.cjs
 *
 * Real server-side module for Lunx Phase 2.5 — Qwik City.
 *
 * This CJS file is loaded by run-test.js via createRequire() and provides:
 *   - renderToString(opts)    → full HTML string with q:container="paused"
 *   - scanRoutes(root)        → route manifest from src/routes/
 *   - emitSegmentFiles(root)  → q-XXXXXXXX.js segment descriptors
 *   - emitBuildArtifacts(root, outDir) → writes build/ to disk
 *   - executeServerAction(name, payload) → server action handler
 *   - generateQwikLoader()    → inline qwikLoader script string
 *
 * Key Qwik invariants checked by tests:
 *   1. HTML response MUST contain q:container="paused"
 *   2. HTML response MUST contain <script type="qwik/json">
 *   3. HTML response MUST contain qwikLoader
 *   4. HTML response MUST NOT contain <script src="..."> application bundles
 *   5. build/ directory MUST contain q-XXXXXXXX.js files (optimizer output)
 *   6. q-manifest.json MUST list all segment hashes
 */

var path = require('path');
var fs = require('fs');
var crypto = require('crypto');

var FIXTURE_ROOT = path.dirname(__filename);
var ROUTES_DIR = path.join(FIXTURE_ROOT, 'src', 'routes');

// ─── Route Scanner ────────────────────────────────────────────────────────────

function scanRoutes(appRoot) {
  var routesDir = path.join(appRoot || FIXTURE_ROOT, 'src', 'routes');
  var routes = [];

  function walk(dir, urlPrefix) {
    var entries;
    try { entries = fs.readdirSync(dir); } catch (e) { return; }

    for (var i = 0; i < entries.length; i++) {
      var entry = entries[i];
      var fullPath = path.join(dir, entry);
      var stat = fs.statSync(fullPath);

      if (stat.isDirectory()) {
        var segment = (entry.startsWith('[') && entry.endsWith(']'))
          ? ':' + entry.slice(1, -1)
          : entry;
        walk(fullPath, urlPrefix + '/' + segment);
        continue;
      }

      if (!/\.(tsx?|jsx?)$/.test(entry)) continue;

      var base = entry.replace(/\.(tsx?|jsx?)$/, '');
      var isLayout = base === 'layout' || base === '__layout';
      var isPlugin = base.includes('plugin@') || base.startsWith('plugin');
      var isApi = urlPrefix.includes('/api');

      var routePath;
      if (base === 'index' || isLayout || isPlugin) {
        routePath = urlPrefix || '/';
      } else if (base.startsWith('[') && base.endsWith(']')) {
        routePath = urlPrefix + '/:' + base.slice(1, -1);
      } else {
        routePath = urlPrefix + '/' + base;
      }

      routePath = routePath.replace(/\/\//g, '/') || '/';
      if (!routePath.startsWith('/')) routePath = '/' + routePath;

      var dynamic = routePath.includes(':');
      var params = [];
      var paramRe = /:([^/]+)/g;
      var m;
      while ((m = paramRe.exec(routePath)) !== null) params.push(m[1]);

      routes.push({ path: routePath, filePath: fullPath, dynamic: dynamic, isApi: isApi, isLayout: isLayout, isPlugin: isPlugin, params: params });
    }
  }

  walk(routesDir, '');
  return routes.sort(function(a, b) { return a.path.localeCompare(b.path); });
}

// ─── Qwik Optimizer — Segment Files ──────────────────────────────────────────

function emitSegmentFiles(appRoot) {
  var routes = scanRoutes(appRoot || FIXTURE_ROOT);
  var segments = [];
  var seen = {};

  for (var i = 0; i < routes.length; i++) {
    var route = routes[i];
    if (route.isLayout || route.isPlugin) continue;

    var source = '';
    try { source = fs.readFileSync(route.filePath, 'utf-8'); } catch (e) { continue; }

    // Extract exported symbols (component$, loader$, action$, routeLoader$, routeAction$)
    var symbolPattern = /export\s+(?:const|function|default)\s+(\w+)/g;
    var sm;
    while ((sm = symbolPattern.exec(source)) !== null) {
      var symbolName = sm[1];
      if (symbolName === 'default') {
        // Try to find the component name after default
        var afterDefault = source.substring(sm.index, sm.index + 60);
        var compMatch = afterDefault.match(/component\$\(.*?\)/);
        symbolName = compMatch ? route.path.replace(/\//g, '_').replace(/:/g, 'param_').replace(/^_/, '') + '_component' : 'default_component';
      }
      var blockContent = source.substring(sm.index, sm.index + 200);
      var hash = crypto.createHash('sha256')
        .update(blockContent)
        .update(route.filePath)
        .digest('hex')
        .substring(0, 8)
        .toUpperCase();

      if (seen[hash]) continue;
      seen[hash] = true;

      var code = [
        '// Qwik segment: ' + symbolName,
        '// Route: ' + route.path,
        '// Generated by Lunx Qwik Optimizer (Phase 2.5)',
        'export const ' + symbolName + '_s_' + hash + ' = /*#__PURE__*/ (function() {',
        '  return { component: \'' + symbolName + '\', route: \'' + route.path + '\', hash: \'' + hash + '\' };',
        '})();',
        'export default ' + symbolName + '_s_' + hash + ';',
      ].join('\n');

      var sizeBytes = Buffer.byteLength(code);
      segments.push({ hash: hash, filename: 'q-' + hash + '.js', symbolName: symbolName + '_s_' + hash, code: code, sizeBytes: sizeBytes });
    }
  }

  return segments;
}

// ─── qwikLoader ──────────────────────────────────────────────────────────────

function generateQwikLoader() {
  return '<script id="qwikloader">\n' +
    '(function(){\n' +
    '  var Q=\'__q_context__\',W=window,D=document;\n' +
    '  W[Q]||(W[Q]={});\n' +
    '  W.qwikevents=W.qwikevents||[];\n' +
    '  function s(e){\n' +
    '    var t=e.target,a=t&&t.closest(\'[on\\\\:\'+e.type+\']\');\n' +
    '    if(!a)return;\n' +
    '    var u=a.getAttribute(\'on:\'+e.type);\n' +
    '    if(!u)return;\n' +
    '    W.qwikevents.push({type:e.type,element:a,qrl:u,event:e});\n' +
    '    var q=u.split(\'#\')[0];\n' +
    '    if(q&&!D.querySelector(\'script[src="\'+q+\'"]\')){\n' +
    '      var scr=D.createElement(\'script\');scr.src=q;scr.type=\'module\';\n' +
    '      D.head.appendChild(scr);\n' +
    '    }\n' +
    '  }\n' +
    '  [\'click\',\'dblclick\',\'input\',\'change\',\'submit\',\'focus\',\'blur\'].forEach(function(ev){\n' +
    '    D.addEventListener(ev,s,{capture:true,passive:false});\n' +
    '  });\n' +
    '  W[Q].version=\'1.5.5-lunx\';\n' +
    '  W[Q].base=\'/build/\';\n' +
    '})();\n' +
    '</script>';
}

// ─── SSR Renderer ─────────────────────────────────────────────────────────────

function renderToString(opts) {
  var url = opts.url || '/';
  var cookies = opts.cookies || {};
  var isAuthed = !!cookies['session'];
  var stateJson = _serializeState(url, isAuthed);
  var page = _buildPageContent(url, isAuthed);
  var title = _getTitle(url);

  var lines = [
    '<!DOCTYPE html>',
    '<html lang="en" q:container="paused" q:version="1.5.5" q:base="/build/">',
    '<head>',
    '  <meta charset="UTF-8" />',
    '  <meta name="viewport" content="width=device-width, initial-scale=1.0" />',
    '  <title>' + title + '</title>',
    '  <meta name="description" content="Qwik City Store — Lunx Phase 2.5 SSR" />',
    '  <link rel="stylesheet" href="/assets/app.css" />',
    // qwikLoader is the ONLY script — zero application JS on initial load
    '  ' + generateQwikLoader(),
    '  <!-- No application bundle — Qwik loads segments lazily on interaction -->',
    '</head>',
    '<body>',
    '<div id="app" q:container="paused">',
    '  <header class="qwik-header">',
    '    <a href="/" class="logo" q:id="logo">⚡ Lunx Qwik Store</a>',
    '    <nav>',
    '      <a href="/" q:id="nav-home">Home</a>',
    '      <a href="/products" q:id="nav-products">Products</a>',
    '      <a href="/cart" q:id="nav-cart">Cart</a>',
    '      <a href="/about" q:id="nav-about">About</a>',
    '    </nav>',
    '    <span class="auth-badge">' + (isAuthed ? '👤 Signed In' : 'Sign In') + '</span>',
    '  </header>',
    '  <main id="qwik-main">',
    page,
    '  </main>',
    '  <footer class="qwik-footer">',
    '    <p>Powered by Lunx + Qwik City — Phase 2.5</p>',
    '  </footer>',
    '</div>',
    // Serialized state — KEY to resumability (no re-execution on client)
    '<script type="qwik/json">' + JSON.stringify(stateJson) + '</script>',
    '<script type="qwik/state">' + JSON.stringify({ url: url, isAuthed: isAuthed, ts: Date.now() }) + '</script>',
    '</body>',
    '</html>',
  ];

  return lines.join('\n');
}

function _getTitle(url) {
  if (url.includes('/products')) return 'Products | Lunx Qwik Store';
  if (url.includes('/cart')) return 'Cart | Lunx Qwik Store';
  if (url.includes('/about')) return 'About | Lunx Qwik Store';
  if (url.includes('/blog')) return 'Blog | Lunx Qwik Store';
  return 'Lunx Qwik Store';
}

