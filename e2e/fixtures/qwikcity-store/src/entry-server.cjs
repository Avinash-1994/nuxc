function _serializeState(url, isAuthed) {
  return {
    ctx: {
      'logo': { text: '⚡ Lunx Qwik Store' },
      'nav-home': { href: '/', active: url === '/' },
      'nav-products': { href: '/products', active: url.includes('/products') },
      'nav-cart': { href: '/cart', active: url.includes('/cart'), count: 0 },
      'hero': { visible: true },
      'hero-title': { text: 'Lunx Qwik Store' },
      'cart-total': { value: 0 },
      'products-section': { loaded: true },
      'features': { loaded: true },
    },
    refs: { currentUrl: url, isAuthed: isAuthed },
    objs: [url, isAuthed, 0, '⚡ Lunx Qwik Store'],
    subs: [],
  };
}

// ─── Server Actions ────────────────────────────────────────────────────────────

function executeServerAction(actionName, payload) {
  switch (actionName) {
    case 'addToCart':
      return Promise.resolve({
        status: 200,
        body: {
          success: true,
          cartId: 'cart-' + crypto.randomBytes(4).toString('hex'),
          productId: payload.productId,
          quantity: payload.quantity || 1,
          total: '$' + (Math.random() * 200 + 50).toFixed(2),
        },
      });
    case 'checkout':
      return Promise.resolve({
        status: 201,
        body: {
          success: true,
          orderId: 'ORD-QWK-' + Math.floor(Math.random() * 90000 + 10000),
          cartId: payload.cartId,
          address: payload.address,
          estimatedDelivery: '3-5 business days',
          total: '$' + (Math.random() * 500 + 100).toFixed(2),
        },
      });
    case 'getProduct':
      return Promise.resolve({
        status: 200,
        body: {
          id: payload.id,
          name: 'Lunx Product ' + payload.id,
          price: '$' + (99 + Number(payload.id) * 10).toFixed(2),
          inStock: true,
          description: 'High-performance Qwik component kit.',
        },
      });
    case 'subscribe':
      return Promise.resolve({
        status: 200,
        body: { success: true, email: payload.email, message: 'Subscribed to Lunx newsletter' },
      });
    default:
      return Promise.resolve({ status: 404, body: { error: "Action '" + actionName + "' not found" } });
  }
}

// ─── Build Artifact Emitter ───────────────────────────────────────────────────

function emitBuildArtifacts(appRoot, outDir) {
  var root = appRoot || FIXTURE_ROOT;
  var buildDir = path.join(outDir, 'build');
  var assetsDir = path.join(outDir, 'assets');
  fs.mkdirSync(buildDir, { recursive: true });
  fs.mkdirSync(assetsDir, { recursive: true });

  var segments = emitSegmentFiles(root);

  // Write each q-XXXXXXXX.js segment file
  for (var i = 0; i < segments.length; i++) {
    var seg = segments[i];
    fs.writeFileSync(path.join(buildDir, seg.filename), seg.code);
  }

  // Write qwikloader standalone
  var loaderCode = generateQwikLoader()
    .replace(/^<script[^>]*>\n?/, '')
    .replace(/\n?<\/script>$/, '');
  fs.writeFileSync(path.join(buildDir, 'qwikloader.js'), loaderCode);

  // Write minimal client bootstrap (NOT sent on initial load)
  var clientEntry = [
    '// Lunx Qwik City — Client Bootstrap',
    '// This file is NOT in the initial HTML response.',
    '// It is loaded lazily when the first segment triggers resumption.',
    '(function resumeQwik() {',
    '  var container = document.getElementById("app");',
    '  if (!container) return;',
    '  container.setAttribute("q:container", "resumed");',
    '  console.log("[Lunx Qwik] Resumed from serialized state");',
    '})();',
  ].join('\n');
  fs.writeFileSync(path.join(buildDir, 'q-client.js'), clientEntry);

  // Write index.html with full SSR output
  var html = renderToString({ url: '/' });
  fs.writeFileSync(path.join(outDir, 'index.html'), html);

  // Write CSS stub
  var css = ':root{--qwik-blue:#2563eb;--bg:#0f172a;--text:#f1f5f9}body{background:var(--bg);color:var(--text);font-family:system-ui}.qwik-header{display:flex;align-items:center;gap:1rem;padding:1rem}.product-grid{display:grid;grid-template-columns:repeat(3,1fr);gap:1rem}';
  fs.writeFileSync(path.join(assetsDir, 'app.css'), css);

  // Write q-manifest.json
  var manifest = {
    version: '1.0.0',
    framework: 'qwik-city',
    lunxPhase: '2.5',
    segments: segments.map(function(s) {
      return { hash: s.hash, filename: 'build/' + s.filename, symbolName: s.symbolName, sizeBytes: s.sizeBytes };
    }),
    generated: new Date().toISOString(),
    zeroJsInitialLoad: true,
    qwikLoaderBytes: Buffer.byteLength(generateQwikLoader()),
  };
  var manifestPath = path.join(outDir, 'q-manifest.json');
  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2));

  // Write build-explain.json
  var routes = scanRoutes(root);
  var explain = {
    adapter: 'qwik-city',
    lunxAdapter: true,
    routes: routes.map(function(r) { return { path: r.path, dynamic: r.dynamic, isApi: r.isApi }; }),
    segmentCount: segments.length,
    zeroJsInitialLoad: true,
    resumable: true,
    qwikLoaderBytes: Buffer.byteLength(generateQwikLoader()),
  };
  fs.writeFileSync(path.join(outDir, 'build-explain.json'), JSON.stringify(explain, null, 2));

  return { segmentCount: segments.length, manifestPath: manifestPath, segments: segments };
}

module.exports = { scanRoutes, emitSegmentFiles, generateQwikLoader, renderToString, executeServerAction, emitBuildArtifacts };
